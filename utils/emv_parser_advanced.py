# Construct-based TLV parser for ASCII EMV payload
emv_tlv_construct = GreedyRange(Struct(
    "tag" / Bytes(2),
    "length" / Int16ub,
    "value" / Bytes(lambda this: this.length)
))

def parse_emv_construct(payload_str: str) -> dict:
    raw = payload_str.encode()
    parsed = emv_tlv_construct.parse(raw)
    data = {}
    for entry in parsed:
        tag = entry.tag.decode()
        val = entry.value.decode()
        if tag == '01': data['chave'] = val
        elif tag == '54': data['valor'] = val
        elif tag == '59': data['merchant_name'] = val
        elif tag == '05': data['txid'] = val
    return data

