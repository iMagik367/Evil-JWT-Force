# python-tlv-based parser
def parse_emv_python_tlv(payload_str: str) -> dict:
    parser = python_tlv.TLVParser(payload_str)
    entries = parser.parse()
    data = {}
    for e in entries:
        tag = e.tag
        val = e.value
        if tag == '01': data['chave'] = val
        elif tag == '54': data['valor'] = val
        elif tag == '59': data['merchant_name'] = val
        elif tag == '05': data['txid'] = val
